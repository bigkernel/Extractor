void TestCaseDecodeQp() {
  const char* s = "hello=20=\r\nworld=0d=0a";
  const char* src = "hello world\r\n";
  std::string out(s);
  int ret = Codecode(Codec::Type::QP, &out);
  assert(ret == SUCCESS);
  assert(out == src);
};

void ReadFile(const char* fname, std::string* out) {
  std::ifstream in(fname);
  assert(in);
  char buf[4096];
  ssize_t size = 0;
  while ((size = in.readsome(buf, 4096)) > 0) {
    out->append(buf, static_cast<size_t>(size));
  }
}

