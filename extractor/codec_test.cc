void TestCaseDecodeUrl() {
  const char* s =
      "%7B%22seat_type%22%3A%22O%22%2C%22seat_"
      "types%22%3A[%22O%22]%2C%22passenger_cou"
      "nt%22%3A1%2C%22old_passenger_str%22%3A%"
      "22%4E%8E%8D%C3%2C340621199204"
      "022812%2C1_%22%2C%22passenger%1%gg";
  const char* src =
      "{\"seat_type\":\"O\",\"seat_types\":[\"O\"],"
      "\"passenger_count\":1,\"old_passenger_str\":"
      "\"\x4E\x8E\x8D\xC3,340621199204022812,1_\",\"passenger%1%gg";
  std::string out(s);
  int ret = Codecode(Codec::Type::URL, &out);
  assert(ret == SUCCESS);
  assert(out == src);
}

void TestCaseDecodeEscape() {
  const char* s =
      "&#23567 \\x68ello \\u5C0F world %u5C0F\\\\ \\\" \\\'";
  std::string src("小 hello 小 world 小\\ \" \'");
  std::string out(s);
  int ret = Codecode(Codec::Type::ESCAPE, &out);
  assert(ret == SUCCESS);
  assert(src == out);
}

